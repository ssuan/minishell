#include "minishell.h"

int	check_space(char *s)
{
	if (*s == '\0')
		return (FALSE);
	while (*s)
	{
		if (*s == 32 || (*s >= 9 && *s <= 13))
			return (FALSE);
		s++;
	}
	return (TRUE);
}
