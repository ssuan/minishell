#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	l;

	l = ft_strlen(s) + 1;
	while (l--)
		if (*(s + l) == (char)c)
			return ((char *)(s + l));
	return (0);
}
